public class whilee
{
    public static void main(String[] args) {

        int n =1;
        while (n<=4)
        {
            System.out.println("Hii "+n);
            int m =1;
            while (m<=3)
            {
                System.out.println("Tejas"+m);
                m++;
            }
            n++;
        }
        System.out.println("Stop "+n);
    }

}